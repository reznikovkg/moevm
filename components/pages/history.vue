    <p>При плановой аттестации университета в декабре 2000 года комиссия признала межвузовский факультет ВГУ прошедшим лицензионную и аттестационную экспертизы, но признала нецелесообразным, не соответствующим Закону об образовавании функционирование факультета на базе двух вузов. Сложности в организации учебного процесса, связанные с проблемами составления и контроля исполнения расписания занятий, различными методическими подходами в преподавании дисциплин и ряд других организационных условий, по мнению комиссии, требовали серьёзных изменений из-за значительных трудностей для студентов, отсутствия определённости за качество подготовки специалистов. На основании проведённого анализа комиссия рекомендовала передать профильные дисциплины в ведение ВГУ, учитывая наличие отдельного корпуса для факультета, хорошей материальной базы, полного комплекта учебного и научного оборудованя, значительных вложений средств в развитие учебного процесса, а после открытия клинической кафедры - определить юридическим лицом фармацевтического факультета ВГУ.</p>
