      Изменения, произошедшие в нашей стране в начале 90-х годов прошлого века, привели к резкому снижению научных разработок, НИИ химии ВГУ остался практически без средств к существованию. Кроме того, бурный рост частной аптечной сети и распад системы распределения молодых специалистов привёл к резкой нехватке фармацевтов и провизоров в регионе. Идею создания фармацевтического факультета на базе классического университета, выдвинутую 
